def timesByOneThousand(number)
    number*1000
end
puts timesByOneThousand(20)

def howOldIwillBein2030(number)
    2030 - (2017 -number)
end
puts howOldIwillBein2030(17)

def makeExcited(string)
    string.upcase + "!!!"
end
puts makeExcited("hello")

def happyBackwards(string)
    string.reverse + ":)"
end
puts happyBackwards("cool")